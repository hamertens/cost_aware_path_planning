_ERGODIC_ITERS_GRID     = [500, 800, 1100, 1400]
_ERGODIC_TRAJSTEPS_GRID = [50, 100, 150]


def get_ergodic_deployment_time(n_samples, inf_criteria, iters, traj_steps, size_cat="small"):
    """
    Returns estimated deployment time for the ErgodicPlanner.

    Parameters
    ----------
    n_samples   : int — current training set size
    inf_criteria: str — 'cohns' or 'variance'
    iters       : int — number of optimisation iterations
    traj_steps  : int — trajectory length in steps
    size_cat    : str — 'small', 'medium', or 'large'

    Returns
    -------
    float — estimated deployment time in seconds
    """
    if size_cat == "medium":
        params_m = ERGODIC_PARAMS_MEDIUM.get(inf_criteria, {})
        if not params_m:
            return 8.0
        iters_c = max(_ERGODIC_ITERS_GRID[0], min(_ERGODIC_ITERS_GRID[-1], iters))
        ts_c    = max(_ERGODIC_TRAJSTEPS_GRID[0], min(_ERGODIC_TRAJSTEPS_GRID[-1], traj_steps))
        iters_lo = max(i for i in _ERGODIC_ITERS_GRID if i <= iters_c)
        iters_hi = min(i for i in _ERGODIC_ITERS_GRID if i >= iters_c)
        ts_lo    = max(t for t in _ERGODIC_TRAJSTEPS_GRID if t <= ts_c)
        ts_hi    = min(t for t in _ERGODIC_TRAJSTEPS_GRID if t >= ts_c)
        def _eval_m(it, ts):
            return _eval_iig_model(n_samples, *params_m[str(it)][str(ts)])
        t_it = (iters_c - iters_lo) / (iters_hi - iters_lo) if iters_hi != iters_lo else 0.0
        t_ts = (ts_c    - ts_lo)    / (ts_hi    - ts_lo)    if ts_hi    != ts_lo    else 0.0
        dt_lo = _eval_m(iters_lo, ts_lo) * (1 - t_ts) + _eval_m(iters_lo, ts_hi) * t_ts
        dt_hi = _eval_m(iters_hi, ts_lo) * (1 - t_ts) + _eval_m(iters_hi, ts_hi) * t_ts
        return dt_lo + t_it * (dt_hi - dt_lo)
    if size_cat == "large":
        params_l = ERGODIC_PARAMS_LARGE.get(inf_criteria, {})
        if not params_l:
            return 8.0
        iters_c = max(_ERGODIC_ITERS_GRID[0], min(_ERGODIC_ITERS_GRID[-1], iters))
        ts_c    = max(_ERGODIC_TRAJSTEPS_GRID[0], min(_ERGODIC_TRAJSTEPS_GRID[-1], traj_steps))
        iters_lo = max(i for i in _ERGODIC_ITERS_GRID if i <= iters_c)
        iters_hi = min(i for i in _ERGODIC_ITERS_GRID if i >= iters_c)
        ts_lo    = max(t for t in _ERGODIC_TRAJSTEPS_GRID if t <= ts_c)
        ts_hi    = min(t for t in _ERGODIC_TRAJSTEPS_GRID if t >= ts_c)
        def _eval_l(it, ts):
            return _eval_iig_model(n_samples, *params_l[str(it)][str(ts)])
        t_it = (iters_c - iters_lo) / (iters_hi - iters_lo) if iters_hi != iters_lo else 0.0
        t_ts = (ts_c    - ts_lo)    / (ts_hi    - ts_lo)    if ts_hi    != ts_lo    else 0.0
        dt_lo = _eval_l(iters_lo, ts_lo) * (1 - t_ts) + _eval_l(iters_lo, ts_hi) * t_ts
        dt_hi = _eval_l(iters_hi, ts_lo) * (1 - t_ts) + _eval_l(iters_hi, ts_hi) * t_ts
        return dt_lo + t_it * (dt_hi - dt_lo)
    if size_cat != "small" or not ERGODIC_PARAMS:
        return 8.0

    params = ERGODIC_PARAMS.get(inf_criteria, {})
    if not params:
        return 8.0

    # Clamp iters and traj_steps to grid bounds
    iters_grid = _ERGODIC_ITERS_GRID
    ts_grid    = _ERGODIC_TRAJSTEPS_GRID

    iters_c = max(iters_grid[0], min(iters_grid[-1], iters))
    ts_c    = max(ts_grid[0],    min(ts_grid[-1],    traj_steps))

    iters_lo = max(i for i in iters_grid if i <= iters_c)
    iters_hi = min(i for i in iters_grid if i >= iters_c)
    ts_lo    = max(t for t in ts_grid    if t <= ts_c)
    ts_hi    = min(t for t in ts_grid    if t >= ts_c)

    def _eval(it, ts):
        return _eval_exp_model(n_samples, *params[str(it)][str(ts)])

    # Bilinear interpolation over (iters, traj_steps)
    t_it = (iters_c - iters_lo) / (iters_hi - iters_lo) if iters_hi != iters_lo else 0.0
    t_ts = (ts_c    - ts_lo)    / (ts_hi    - ts_lo)    if ts_hi    != ts_lo    else 0.0

    dt_lo = _eval(iters_lo, ts_lo) * (1 - t_ts) + _eval(iters_lo, ts_hi) * t_ts
    dt_hi = _eval(iters_hi, ts_lo) * (1 - t_ts) + _eval(iters_hi, ts_hi) * t_ts
    return dt_lo + t_it * (dt_hi - dt_lo)


