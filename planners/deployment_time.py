def _nearest_horizon_key(params_dict, horizon):
    """Return the key in params_dict (stored as str ints) nearest to horizon."""
    keys = sorted(int(k) for k in params_dict)
    return str(min(keys, key=lambda k: abs(k - int(horizon))))


def _eval_exp_model(n, a, b, c):
    return a * np.exp(b * n) + c


def _eval_model(n, a, b, c, d):
    return a * np.power(max(n, 1), b) * np.exp(-c * max(n, 1)) + d


def _eval_dyn_ktbo_model(n, a, b, c, d):
    """f(n) = a * exp(-b * n) + c * n + d"""
    return a * np.exp(-b * n) + c * n + d


def get_ktbo_deployment_time(n_samples, inf_criteria, simulations, size_cat="small"):
    """
    Returns estimated deployment time for the KTBO planner.

    Parameters
    ----------
    n_samples   : int   — current training set size
    inf_criteria: str   — 'cohns', 'variance', 'dyn_cohns', or 'dyn_variance'
    simulations : int   — number of skopt iterations (Optuna-tuned)
    size_cat    : str   — 'small', 'medium', or 'large'

    Returns
    -------
    float — estimated deployment time in seconds
    """
    if size_cat == "medium":
        sim_params = KTBO_PARAMS_MEDIUM.get(inf_criteria)
        if not sim_params:
            return _KTBO_DYN_FALLBACK
        sims_clamped = max(_KTBO_SIMULATIONS_GRID[0], min(_KTBO_SIMULATIONS_GRID[-1], simulations))
        lower = max(s for s in _KTBO_SIMULATIONS_GRID if s <= sims_clamped)
        upper = min(s for s in _KTBO_SIMULATIONS_GRID if s >= sims_clamped)
        n = float(n_samples)
        dt_lower = _eval_iig_model(n, *sim_params[str(lower)])
        if lower == upper:
            return dt_lower
        dt_upper = _eval_iig_model(n, *sim_params[str(upper)])
        t = (sims_clamped - lower) / (upper - lower)
        return dt_lower + t * (dt_upper - dt_lower)
    if size_cat == "large":
        sim_params_l = KTBO_PARAMS_LARGE.get(inf_criteria)
        if not sim_params_l:
            return _KTBO_DYN_FALLBACK
        sims_clamped = max(_KTBO_SIMULATIONS_GRID[0], min(_KTBO_SIMULATIONS_GRID[-1], simulations))
        lower = max(s for s in _KTBO_SIMULATIONS_GRID if s <= sims_clamped)
        upper = min(s for s in _KTBO_SIMULATIONS_GRID if s >= sims_clamped)
        n = float(n_samples)
        dt_lower = _eval_iig_model(n, *sim_params_l[str(lower)])
        if lower == upper:
            return dt_lower
        dt_upper = _eval_iig_model(n, *sim_params_l[str(upper)])
        t = (sims_clamped - lower) / (upper - lower)
        return dt_lower + t * (dt_upper - dt_lower)
    if size_cat != "small":
        return 5.0

    if inf_criteria in ("dyn_cohns", "dyn_variance"):
        sim_params = DYN_KTBO_PARAMS.get(inf_criteria)
        if not sim_params:
            return _KTBO_DYN_FALLBACK

        sims_clamped = max(_KTBO_SIMULATIONS_GRID[0], min(_KTBO_SIMULATIONS_GRID[-1], simulations))
        lower = max(s for s in _KTBO_SIMULATIONS_GRID if s <= sims_clamped)
        upper = min(s for s in _KTBO_SIMULATIONS_GRID if s >= sims_clamped)

        n = float(n_samples)
        dt_lower = _eval_dyn_ktbo_model(n, *sim_params[str(lower)])
        if lower == upper:
            return dt_lower

        dt_upper = _eval_dyn_ktbo_model(n, *sim_params[str(upper)])
        t = (sims_clamped - lower) / (upper - lower)
        return dt_lower + t * (dt_upper - dt_lower)

    params = KTBO_PARAMS[inf_criteria]

    def _eval_power_decay(n, s):
        a, b, c = params[str(s)]
        return a * (n + 1) ** (-b) + c

    sims_clamped = max(_KTBO_SIMULATIONS_GRID[0], min(_KTBO_SIMULATIONS_GRID[-1], simulations))
    lower = max(s for s in _KTBO_SIMULATIONS_GRID if s <= sims_clamped)
    upper = min(s for s in _KTBO_SIMULATIONS_GRID if s >= sims_clamped)

    dt_lower = _eval_power_decay(n_samples, lower)
    if lower == upper:
        return dt_lower

    dt_upper = _eval_power_decay(n_samples, upper)
    t = (sims_clamped - lower) / (upper - lower)
    return dt_lower + t * (dt_upper - dt_lower)


def _eval_rig_entry(n, entry):
    """Dispatch to the correct model based on the tag stored as entry[0]."""
    tag, a, b, c, d = entry[0], entry[1], entry[2], entry[3], entry[4]
    if tag == "exp":
        return a * np.exp(-b * n) + c * n + d
    return _eval_model(n, a, b, c, d)  # "power": a * n^b * exp(-c*n) + d


def get_rig_deployment_time(n_samples, inf_criteria, horizon, samples, size_cat="small"):
    """
    Returns estimated deployment time for the RIG planner.

    Parameters
    ----------
    n_samples   : int   — current training set size
    inf_criteria: str   — 'cohns' or 'variance'
    horizon     : int   — step limit (2–6)
    samples     : int   — number of RIG tree samples (Optuna-tuned)
    size_cat    : str   — 'small', 'medium', or 'large'

    Returns
    -------
    float — estimated deployment time in seconds
    """
    if size_cat == "medium":
        horizon_params = RIG_PARAMS_MEDIUM[inf_criteria][str(horizon)]
        samples_clamped = max(_SAMPLES_GRID[0], min(_SAMPLES_GRID[-1], samples))
        lower = max(s for s in _SAMPLES_GRID if s <= samples_clamped)
        upper = min(s for s in _SAMPLES_GRID if s >= samples_clamped)
        dt_lower = _eval_iig_model(n_samples, *horizon_params[str(lower)])
        if lower == upper:
            return dt_lower
        dt_upper = _eval_iig_model(n_samples, *horizon_params[str(upper)])
        t = (samples_clamped - lower) / (upper - lower)
        return dt_lower + t * (dt_upper - dt_lower)
    if size_cat == "large":
        horizon_params_l = RIG_PARAMS_LARGE[inf_criteria][str(horizon)]
        samples_clamped = max(_SAMPLES_GRID[0], min(_SAMPLES_GRID[-1], samples))
        lower = max(s for s in _SAMPLES_GRID if s <= samples_clamped)
        upper = min(s for s in _SAMPLES_GRID if s >= samples_clamped)
        dt_lower = _eval_iig_model(n_samples, *horizon_params_l[str(lower)])
        if lower == upper:
            return dt_lower
        dt_upper = _eval_iig_model(n_samples, *horizon_params_l[str(upper)])
        t = (samples_clamped - lower) / (upper - lower)
        return dt_lower + t * (dt_upper - dt_lower)
    if size_cat != "small":
        return 5.0

    horizon_params = RIG_PARAMS[inf_criteria][str(horizon)]

    # Clamp to grid bounds
    samples_clamped = max(_SAMPLES_GRID[0], min(_SAMPLES_GRID[-1], samples))

    # Find the two bracketing grid points
    lower = max(s for s in _SAMPLES_GRID if s <= samples_clamped)
    upper = min(s for s in _SAMPLES_GRID if s >= samples_clamped)

    dt_lower = _eval_rig_entry(n_samples, horizon_params[str(lower)])

    if lower == upper:
        return dt_lower

    dt_upper = _eval_rig_entry(n_samples, horizon_params[str(upper)])

    # Linear interpolation
    t = (samples_clamped - lower) / (upper - lower)
    return dt_lower + t * (dt_upper - dt_lower)


def get_mcts_deployment_time(n_samples, inf_criteria, horizon, simulations, size_cat="small"):
    """
    Returns estimated deployment time for the MCTS planner.

    Parameters
    ----------
    n_samples   : int   — current training set size
    inf_criteria: str   — 'cohns', 'variance', 'dyn_cohns', or 'dyn_variance'
    horizon     : int   — planning horizon (3–6 for cohns/variance)
    simulations : int   — number of MCTS simulations (Optuna-tuned)
    size_cat    : str   — 'small', 'medium', or 'large'

    Returns
    -------
    float — estimated deployment time in seconds
    """
    if size_cat == "medium":
        crit_params = MCTS_PARAMS_MEDIUM.get(inf_criteria, {})
        if not crit_params:
            return 10.0
        grid = _DYN_MCTS_SIMULATIONS_GRID if inf_criteria in ("dyn_cohns", "dyn_variance") else _MCTS_SIMULATIONS_GRID
        h_key = _nearest_horizon_key(crit_params, horizon)
        params = crit_params[h_key]
        sims_clamped = max(grid[0], min(grid[-1], simulations))
        lower = max(s for s in grid if s <= sims_clamped)
        upper = min(s for s in grid if s >= sims_clamped)
        dt_lower = _eval_iig_model(n_samples, *params[str(lower)])
        if lower == upper:
            return dt_lower
        dt_upper = _eval_iig_model(n_samples, *params[str(upper)])
        t = (sims_clamped - lower) / (upper - lower)
        return dt_lower + t * (dt_upper - dt_lower)
    if size_cat == "large":
        crit_params_l = MCTS_PARAMS_LARGE.get(inf_criteria, {})
        if not crit_params_l:
            return 10.0
        grid = _DYN_MCTS_SIMULATIONS_GRID if inf_criteria in ("dyn_cohns", "dyn_variance") else _MCTS_SIMULATIONS_GRID
        h_key = _nearest_horizon_key(crit_params_l, horizon)
        params_l = crit_params_l[h_key]
        sims_clamped = max(grid[0], min(grid[-1], simulations))
        lower = max(s for s in grid if s <= sims_clamped)
        upper = min(s for s in grid if s >= sims_clamped)
        dt_lower = _eval_iig_model(n_samples, *params_l[str(lower)])
        if lower == upper:
            return dt_lower
        dt_upper = _eval_iig_model(n_samples, *params_l[str(upper)])
        t = (sims_clamped - lower) / (upper - lower)
        return dt_lower + t * (dt_upper - dt_lower)
    if size_cat != "small":
        return 10.0

    if inf_criteria in ("dyn_cohns", "dyn_variance"):
        params = DYN_MCTS_PARAMS[inf_criteria][str(horizon)]
        grid = _DYN_MCTS_SIMULATIONS_GRID
    else:
        params = MCTS_PARAMS[inf_criteria][str(horizon)]
        grid = _MCTS_SIMULATIONS_GRID

    # Clamp to grid bounds and interpolate
    sims_clamped = max(grid[0], min(grid[-1], simulations))
    lower = max(s for s in grid if s <= sims_clamped)
    upper = min(s for s in grid if s >= sims_clamped)

    dt_lower = _eval_exp_model(n_samples, *params[str(lower)])

    if lower == upper:
        return dt_lower

    dt_upper = _eval_exp_model(n_samples, *params[str(upper)])

    t = (sims_clamped - lower) / (upper - lower)
    return dt_lower + t * (dt_upper - dt_lower)


