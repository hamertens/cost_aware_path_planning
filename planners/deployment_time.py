_IIG_FALLBACK = 3.0

_IIG_RIC_GRID = [0.01, 0.03, 0.05, 0.07, 0.1]


def _eval_iig_model(n, a, b, c, d):
    """f(n) = a * exp(-b * n) + c * n + d"""
    return a * np.exp(-b * n) + c * n + d


def get_iig_deployment_time(n_samples, inf_criteria, horizon, ric, size_cat="small"):
    """
    Returns estimated deployment time for the IIG planner.

    Parameters
    ----------
    n_samples   : int   — current training set size
    inf_criteria: str   — 'dyn_cohns' or 'dyn_variance'
    horizon     : int   — planning horizon (2, 3, or 4)
    ric         : float — running information cost; clamped to [0.01, 0.1] and
                          linearly interpolated between fitted grid points
                          {0.01, 0.03, 0.05, 0.07, 0.1}
    size_cat    : str   — 'small', 'medium', or 'large'

    Returns
    -------
    float — estimated deployment time in seconds
    """
    if size_cat == "medium":
        crit_params_m = IIG_PARAMS_MEDIUM.get(inf_criteria, {})
        if not crit_params_m:
            return _IIG_FALLBACK
        h_key = _nearest_horizon_key(crit_params_m, horizon)
        horizon_params_m = crit_params_m.get(h_key)
        if horizon_params_m is None:
            return _IIG_FALLBACK
        ric_grid_m = sorted(float(k) for k in horizon_params_m)
        ric_clamped = max(ric_grid_m[0], min(ric_grid_m[-1], ric))
        lower = max(r for r in ric_grid_m if r <= ric_clamped)
        upper = min(r for r in ric_grid_m if r >= ric_clamped)
        n = float(n_samples)
        dt_lower = _eval_iig_model(n, *horizon_params_m[str(lower)])
        if lower == upper:
            return dt_lower
        dt_upper = _eval_iig_model(n, *horizon_params_m[str(upper)])
        t = (ric_clamped - lower) / (upper - lower)
        return dt_lower + t * (dt_upper - dt_lower)
    if size_cat == "large":
        crit_params_l = IIG_PARAMS_LARGE.get(inf_criteria, {})
        if not crit_params_l:
            return _IIG_FALLBACK
        h_key_l = _nearest_horizon_key(crit_params_l, horizon)
        horizon_params_l = crit_params_l.get(h_key_l)
        if horizon_params_l is None:
            return _IIG_FALLBACK
        ric_grid_l = sorted(float(k) for k in horizon_params_l)
        ric_clamped = max(ric_grid_l[0], min(ric_grid_l[-1], ric))
        lower = max(r for r in ric_grid_l if r <= ric_clamped)
        upper = min(r for r in ric_grid_l if r >= ric_clamped)
        n = float(n_samples)
        dt_lower = _eval_iig_model(n, *horizon_params_l[str(lower)])
        if lower == upper:
            return dt_lower
        dt_upper = _eval_iig_model(n, *horizon_params_l[str(upper)])
        t = (ric_clamped - lower) / (upper - lower)
        return dt_lower + t * (dt_upper - dt_lower)
    if size_cat != "small":
        return _IIG_FALLBACK

    horizon_params = IIG_PARAMS.get(inf_criteria, {}).get(str(horizon))
    if horizon_params is None:
        return _IIG_FALLBACK

    ric_clamped = max(_IIG_RIC_GRID[0], min(_IIG_RIC_GRID[-1], ric))
    lower = max(r for r in _IIG_RIC_GRID if r <= ric_clamped)
    upper = min(r for r in _IIG_RIC_GRID if r >= ric_clamped)

    n = float(n_samples)
    dt_lower = _eval_iig_model(n, *horizon_params[str(lower)])
    if lower == upper:
        return dt_lower

    dt_upper = _eval_iig_model(n, *horizon_params[str(upper)])
    t = (ric_clamped - lower) / (upper - lower)
    return dt_lower + t * (dt_upper - dt_lower)
